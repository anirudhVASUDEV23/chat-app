import mongoose from "mongoose";

const userSchema = new mongoose.Schema(
  {
    email: {
      type: String,
      required: true,
      unique: true,
    },
    fullName: {
      type: String,
      required: true,
    },
    password: {
      type: String,
      required: true,
      minlength: 6,
    },
    profilePic: {
      type: String,
      default: "",
    },
  },
  { timestamps: true } // Add timestamps for createdAt and updatedAt
);

const User = mongoose.model("User", userSchema); //mongodb converts it into plural form and lowercase it, so the collection name will be "users"
export default User;
